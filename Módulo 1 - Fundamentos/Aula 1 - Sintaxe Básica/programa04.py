#Aritméticos
x = 10
y = 3

print(x + y) #Soma
print(x - y) #Subtração
print(x * y) #Multiplicação
print(x / y) #Divisão
print(x // y) #Divisão Inteira
print(x % y) #Resto da Divisão
print(x ** y) #Potencia

#Comparação
#Retornam True ou False
print(x == y) #igual
print(x != y) #diferente
print(x > y) #maior
print(x < y ) #menor
print(x >= y) #maior ou igual
print(x <= y) #menor ou igual

#Lógicos
print(True and False) # E
print(True or False) #OU
print(not True) # NOT
