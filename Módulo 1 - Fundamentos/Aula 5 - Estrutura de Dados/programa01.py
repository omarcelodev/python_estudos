# LISTAS

# Criando Listas
numeros = [1, 2, 3, 4]
mistura = [10, "oi", 3.14, True]

# Acessando elementos(índice começa em 0)
print(numeros[0]) #1
print(mistura[1]) # "oi"

# Alterando Valores
numeros[2] = 99
print(numeros) # [1, 2, 99, 4]

#Métodos úteis
numeros.append(5) # adiciona
numeros.remove(99) # remove valor

print(len(numeros)) # tamanho da lista

